    private void pbtnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pbtnSubmitActionPerformed
        // TODO add your handling code here:
        String ptName = pName.getText();
        String ptHeight= pHeight.getText();
        String ptWeight= pWeight.getText();
        String ptPhone = pPhone.getText();
        String ptEmail = pEmail.getText();
        String ptAddress = pAddress.getText();
        String ptPincode = pPincode.getText();
        String ptGender = null;
        if(Male.isSelected()){
            ptGender = "Male";
        }else if(Female.isSelected()){
            ptGender = "Female";
        }else if(Other.isSelected()){
            ptGender = "Other";
        }
        
        if(ptName.isEmpty() || ptHeight.isEmpty() || ptWeight.isEmpty() || ptPhone.isEmpty()|| 
                ptEmail.isEmpty()|| ptAddress.isEmpty() || ptPincode.isEmpty()){
            JOptionPane.showMessageDialog(this,"Fill up the form Properly.", "Error",JOptionPane.ERROR_MESSAGE);
        }else{
            patientRegister(ptName,ptGender,ptHeight,ptWeight,ptPhone,ptEmail,ptAddress,ptPincode);
        }
    }//GEN-LAST:event_pbtnSubmitActionPerformed

