    private void patientRegister(String ptName, String ptGender, String ptHeight,
            String ptWeight, String ptPhone, String ptEmail, String ptAddress, String ptPincode) {
        
         Connection dbcon = database.mycon();
        if(dbcon != null ){
        try { 
            PreparedStatement st=(PreparedStatement)dbcon.prepareStatement
        ("INSERT INTO patient_data (Name,Gender,Height,Weight,Phone,Email,Address,Pincode) VALUES(?,?,?,?,?,?,?,?)");
            
            st.setString(1, ptName);
            st.setString(2, ptGender);
            st.setString(3, ptHeight);
            st.setString(4, ptWeight);
            st.setString(5, ptPhone);
            st.setString(6, ptEmail);
            st.setString(7, ptAddress);
            st.setString(8, ptPincode);
           
            int res=st.executeUpdate();
            JOptionPane.showMessageDialog(this,"User data inserted.","Success",JOptionPane.INFORMATION_MESSAGE);
           
        } catch (SQLException ex) {
          System.out.print(ex);
        }
        }else{
            System.out.println("The connection not available.");
        }
    }
